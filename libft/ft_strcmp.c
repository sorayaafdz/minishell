#include "libft.h"

int	ft_strcmp(char const *s1, char const *s2)
{
	int	i;

	i = 0;
	if (!s2 || !s1)
		return (1);
	while (s1[i] && (s1[i] == s2[i]))
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
